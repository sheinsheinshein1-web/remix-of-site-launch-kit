import fs from "node:fs";
import path from "node:path";
import sharp from "sharp";

const root = process.cwd();
const projectsPath = path.join(root, "src/data/projects.ts");
const regionalPath = path.join(root, "src/data/regionalBatchProjects.ts");
const outPath = path.join(root, "src/data/projectThumbs.ts");
const projectsSource = fs.readFileSync(projectsPath, "utf8");
const regionalSource = fs.readFileSync(regionalPath, "utf8");

const importByVar = new Map();
for (const match of projectsSource.matchAll(/^import\s+(\w+)\s+from\s+"@\/assets\/([^"]+)";/gm)) {
  importByVar.set(match[1], match[2]);
}

const refs = [];
const mainProjectRe = /id:\s*(\d+),\s*name:[\s\S]*?gallery:\s*\[\s*\{\s*image:\s*(\w+)/g;
for (const match of projectsSource.matchAll(mainProjectRe)) {
  const id = Number(match[1]);
  const variable = match[2];
  const assetRel = importByVar.get(variable);
  if (assetRel) refs.push({ id, assetRel });
}

const regionalRe = /id:\s*(\d+),[\s\S]*?assetFolder:\s*"([^"]+)"/g;
for (const match of regionalSource.matchAll(regionalRe)) {
  const id = Number(match[1]);
  const folder = match[2];
  const absFolder = path.join(root, "src/assets", folder);
  if (!fs.existsSync(absFolder)) continue;
  const first = fs.readdirSync(absFolder)
    .filter((file) => /\.(webp|png|jpe?g)$/i.test(file) && !/__thumb\.webp$/i.test(file))
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }))[0];
  if (first) refs.push({ id, assetRel: `${folder}/${first}` });
}

const seen = new Set();
const uniqueRefs = refs
  .filter((ref) => {
    if (seen.has(ref.id)) return false;
    seen.add(ref.id);
    return true;
  })
  .sort((a, b) => a.id - b.id);

let before = 0;
let after = 0;
let generated = 0;
let reused = 0;
const imports = [];
const entries = [];

for (const ref of uniqueRefs) {
  const sourceAbs = path.join(root, "src/assets", ref.assetRel);
  if (!fs.existsSync(sourceAbs)) {
    console.warn(`skip missing ${ref.id}: ${ref.assetRel}`);
    continue;
  }

  const parsed = path.parse(sourceAbs);
  const thumbAbs = path.join(parsed.dir, `${parsed.name}__thumb.webp`);
  before += fs.statSync(sourceAbs).size;

  if (!fs.existsSync(thumbAbs) || fs.statSync(thumbAbs).mtimeMs < fs.statSync(sourceAbs).mtimeMs) {
    await sharp(sourceAbs)
      .rotate()
      .resize({ width: 640, withoutEnlargement: true })
      .webp({ quality: 72, effort: 5 })
      .toFile(thumbAbs);
    generated += 1;
  } else {
    reused += 1;
  }

  after += fs.statSync(thumbAbs).size;
  const thumbRel = path.relative(path.join(root, "src/assets"), thumbAbs).split(path.sep).join("/");
  const importName = `thumb${ref.id}`;
  imports.push(`import ${importName} from "@/assets/${thumbRel}";`);
  entries.push(`  ${ref.id}: ${importName},`);
}

const content = `// Auto-generated by scripts/generate-card-thumbnails.mjs.
// Maps project id to the lightweight first image used in project cards.

${imports.join("\n")}

export const projectThumbs: Record<number, string> = {
${entries.join("\n")}
};
`;

fs.writeFileSync(outPath, content);

console.log(JSON.stringify({
  projects: uniqueRefs.length,
  generated,
  reused,
  beforeMB: +(before / 1024 / 1024).toFixed(2),
  afterMB: +(after / 1024 / 1024).toFixed(2),
  savedMB: +((before - after) / 1024 / 1024).toFixed(2),
  outPath,
}, null, 2));
